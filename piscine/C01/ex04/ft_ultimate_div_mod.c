void	ft_ultimate_div_mod(int *a, int *b)
{
	int temp;

	temp = 0;
	if (*b <= 0)
	{
		*a = 0;
		*b = 0;
		return ;
	}
	temp = *a;
	*a = *a / *b;
	*b = temp % *b;
}

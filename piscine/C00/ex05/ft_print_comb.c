#include <unistd.h>

void	ft_print(char *b)
{
	if (b[2] == '9')
	{
		if (b[1] != '8')
		{
			b[1] = b[1] + 1;
			b[2] = b[1];
		}
		else
		{
			b[0] = b[0] + 1;
			b[1] = b[0] + 1;
			b[2] = b[1];
		}
	}
}

void	ft_print_comb(void)
{
	char	b[3];

	b[0] = '0';
	b[1] = '1';
	b[2] = '2';
	while (b[0] <= '7')
	{
		while (b[1] <= '8')
		{
			while (b[2] <= '9')
			{
				write(1, &b, 3);
				if (!(b[0] == '7' && b[1] == '8' && b[2] == '9'))
				{
					write(1, ",", 1);
					write(1, " ", 1);
				}
				ft_print(b);
				b[2]++;
			}
		}
	}
}

int main(void)
{
	ft_print_comb();
}

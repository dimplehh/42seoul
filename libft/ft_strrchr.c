char	*ft_strrchr(const char *s, int c)
{
	int i;

	i = 0;
	while (s[i])
		i++;
	while (*s != '\0')
		s++;
	while (*s != (char)c)
	{
		if (i <= 0)
			return (0);
		s--;
		i--;
	}
	return ((char *)s);
}
